    private void searchTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTextFieldKeyReleased
        // TODO add your handling code here:
           try{
            Connection con=DBconnectTeachers.GetConnection();
            String quary="SELECT * FROM studentpayment WHERE StudentRoll LIKE'%"+studentrollTextField.getText()+"'";
        PreparedStatement  ps=con.prepareStatement(quary);
         ResultSet rs = ps.executeQuery();
         if(rs.next())
         {
             String add1=rs.getString("StudentRoll");
             studentrollTextField.setText(add1);
              String add2=rs.getString("StudentName");
             studentnameTextField.setText(add2);
              String add3=rs.getString("Slass");
             classTextField.setText(add3);
              String add4=rs.getString("Payment");
             paymentTextField.setText(add4);
              String add5=rs.getString("TotalDue");
             totaldueTextField.setText(add5);
             
         }
        }catch(Exception e)
        {
             JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_searchTextFieldKeyReleased

