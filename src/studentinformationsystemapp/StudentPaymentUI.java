    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        if(studentrollTextField.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please Fill up StudentRoll");
        }
        else if (studentnameTextField.getText().equals(""))
        {
              JOptionPane.showMessageDialog(this, "Please Fill up Student Name");
        }
        else if (classTextField.getText().equals(""))
        {
              JOptionPane.showMessageDialog(this, "Please Fill up Student Class");
        }
        else if (paymentTextField.getText().equals(""))
        {
              JOptionPane.showMessageDialog(this, "Please Fill up payment");
        }
        else if (totaldueTextField.getText().equals(""))
        {
              JOptionPane.showMessageDialog(this, "Please Fill up TotalDue");
        }
        else
        {
            try{
                Statement stmt = null;
                Connection con =DBconnectTeachers.GetConnection();
                stmt=con.createStatement();
                String quary = "INSERT INTO studentpayment VALUES ('"+studentrollTextField.getText()+"','"+studentnameTextField.getText()+"','"+classTextField.getText()+"','"+DateChooser.getDate()+"','"+paymentTextField.getText()+"','"+totaldueTextField.getText()+"','"+reasonComboBox.getSelectedItem().toString()+"')";
                int RowsAffected=stmt.executeUpdate(quary);
                  JOptionPane.showMessageDialog(this,RowsAffected+"New regiration" );
                        studentrollTextField.setText("");
                       studentnameTextField.setText("");
                       classTextField.setText("");
                        paymentTextField.setText("");
                       totaldueTextField.setText("");
     
            }catch(Exception e)
            {
                Logger.getLogger(StudentPaymentUI.class.getName()).log(Level.SEVERE, null,e);
            }
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

