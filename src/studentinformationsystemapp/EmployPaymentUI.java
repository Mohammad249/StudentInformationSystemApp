    private void searchTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTextFieldKeyReleased
        // TODO add your handling code here:
         try{
                Connection con= DBconnectTeachers.GetConnection();
                String quary="SELECT * FROM eemployeepayment WHERE EmployeeId LIKE '%"+searchTextField.getText()+"'";
                  PreparedStatement ps=con.prepareStatement(quary);
               ResultSet rs = ps.executeQuery();
               if(rs.next())
               {
                   String add1=rs.getString("EmployeeId");
                   employeeIdTextField.setText(add1);
                   String add2=rs.getString("Employeename");
                   employeenameTextField.setText(add2);  
                    String add3=rs.getString("position");
                   positionTextField.setText(add3);
                   String add4=rs.getString("Payment");
                   paymentTextField.setText(add4);
                   String add5=rs.getString("TotalDue");                 
                   totaldueTxtField.setText(add5); 
               }
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }
    }//GEN-LAST:event_searchTextFieldKeyReleased

