    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
         if(employeeIdTextField.getText().equals(""))
         {
             JOptionPane.showMessageDialog(this, "Please FillUp Employee Id");
         }
      else if(employeenameTextField.getText().equals(""))
         {
             JOptionPane.showMessageDialog(this, "Please FillUp Employee Name");
         }
         else if(positionTextField.getText().equals(""))
         {
             JOptionPane.showMessageDialog(this, "Please FillUp position");
         }
         else if(paymentTextField.getText().equals(""))
         {
              JOptionPane.showMessageDialog(this, "Please FillUp Payment");
         }
          else if(totaldueTxtField.getText().equals(""))
         {
              JOptionPane.showMessageDialog(this, "Please FillUp total Due");
         }
          else
          {
              try
              {
                  Statement stmt = null;
                  Connection con = DBconnectTeachers.GetConnection();
                  stmt =con.createStatement();
                  String quary="INSERT INTO eemployeepayment VALUES('"+employeeIdTextField.getText()+"','"+employeenameTextField.getText()+"','"+positionTextField.getText()+"','"+DateChooser.getDate()+"','"+paymentTextField.getText()+"','"+totaldueTxtField.getText()+"')";
                  int RowsAffected=stmt.executeUpdate(quary);
                     JOptionPane.showMessageDialog(this,RowsAffected+"New regiration" );
                            employeeIdTextField.setText("");
                            employeenameTextField.setText("");
                             positionTextField.setText("");
                             paymentTextField.setText("");
                             totaldueTxtField.setText("");
              }catch(Exception e)
              {
                  Logger.getLogger(EmployPaymentUI.class.getName()).log(Level.SEVERE, null,e);
              }
          }
    }//GEN-LAST:event_saveButtonActionPerformed

