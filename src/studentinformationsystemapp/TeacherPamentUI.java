    private void SearchTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchTextFieldKeyReleased
        // TODO add your handling code here:
           try{
                Connection con= DBconnectTeachers.GetConnection();
                String quary="SELECT * FROM teacherpayment WHERE TeacherId LIKE '%"+SearchTextField.getText()+"'";
                  PreparedStatement ps=con.prepareStatement(quary);
               ResultSet rs = ps.executeQuery();
               if(rs.next())
               {
                   String add1=rs.getString("TeacherId");
                   teacheidTextField.setText(add1);
                   String add2=rs.getString("TeacherName");
                   teachernameTextField.setText(add2);
                   String add3=rs.getString("Payment");
                   paymentTextField.setText(add3);
                   String add4=rs.getString("TotalDue");
                   totaldueTextField.setText(add4);
                   
                   
                   
               }
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }
        
    }//GEN-LAST:event_SearchTextFieldKeyReleased

