    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
         if(teacheidTextField.getText().equals(""))
         {
             JOptionPane.showMessageDialog(this, "Please FillUp Teacher Id");
         }
         else if(teachernameTextField.getText().equals(""))
         {
             JOptionPane.showMessageDialog(this, "Please FillUp Teacher Name");
         }
         else if(paymentTextField.getText().equals(""))
         {
              JOptionPane.showMessageDialog(this, "Please FillUp Payment");
         }
          else if(totaldueTextField.getText().equals(""))
         {
              JOptionPane.showMessageDialog(this, "Please FillUp total Due");
         }
         else
          {
              try
              {
                  Statement stmt = null;
                  Connection con = DBconnectTeachers.GetConnection();
                  stmt =con.createStatement();
                  String quary="INSERT INTO teacherpayment VALUES('"+teacheidTextField.getText()+"','"+teachernameTextField.getText()+"','"+dateDateChooser.getDate()+"','"+paymentTextField.getText()+"','"+totaldueTextField.getText()+"')";
                  int RowsAffected=stmt.executeUpdate(quary);
                     JOptionPane.showMessageDialog(this,RowsAffected+"New regiration" );
                            teacheidTextField.setText("");
                            teachernameTextField.setText("");
                            paymentTextField.setText("");
                            totaldueTextField.setText("");
              }catch(Exception e)
              {
                  Logger.getLogger(TeacherPamentUI.class.getName()).log(Level.SEVERE, null,e);
              }
          }
    }//GEN-LAST:event_saveButtonActionPerformed

