        // TODO add your handling code here:
         try{
              Connection con = DBconnectTeachers.GetConnection();
              String quary = "SELECT * FROM studentmarks WHERE RollNo LIKE '%"+RollNoTextField.getText()+"' " ; 
              
             PreparedStatement ps=con.prepareStatement(quary);

              ResultSet rs = ps.executeQuery();
              if(rs.next())
              {
                  String add1=rs.getString("RollNo");
                  nameLabel.setText(add1);
                   String add2=rs.getString("Name");
                  rollLabel.setText(add2);
                   String add3=rs.getString("Slass");
                  slasslLabel1.setText(add3);
                   String add4=rs.getString("Bangla");
                  BanglaLabel.setText(add4);
                   String add5=rs.getString("English");
                  englishLabel.setText(add5);
                     String add6=rs.getString("Math");
                  mathLabel.setText(add6);
                     String add7=rs.getString("Science");
                  scienceLabel.setText(add7);
                     String add8=rs.getString("Islam");
                  islamLabel.setText(add8);
                 
                     String add9=rs.getString("Ect");
                   ictLabel.setText(add9);
                     
                   
                  
              }
          }catch(Exception e)
          {
                     JOptionPane.showMessageDialog(this, e );
          }
        
